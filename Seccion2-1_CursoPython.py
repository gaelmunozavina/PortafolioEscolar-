print("CAPÍTULO 2.1 - Introducción a la función print()")

print("\n1. El primer programa en Python:")
print('   print("¡Hola, Mundo!")')

print("\n2. print() es una función integrada de Python.")
print("   Se usa para mostrar texto o datos en la pantalla.")

print("\n3. Partes del primer programa:")
print("   - La palabra print")
print("   - Paréntesis ()")
print("   - Comillas")
print("   - Una cadena de texto")

print("\n4. Una función puede:")
print("   - Causar un efecto (mostrar texto, crear archivos, etc.)")
print("   - Devolver un valor")

print("\n5. Argumentos:")
print("   Son los datos que se colocan dentro de los paréntesis.")
print('   Ejemplo: print("Hola")')

print("\n6. Cadenas:")
print("   Son textos dentro de comillas.")
print('   Ejemplo: "Hola mundo" o \'Hola mundo\'')

print("\n7. Instrucciones:")
print("   Un programa es un conjunto de instrucciones.")
print("   En Python normalmente hay una instrucción por línea.")

print("\n8. Salto de línea con \\n:")
print("   Permite imprimir texto en varias líneas.")
print("   Ejemplo:")
print("   Hola\nMundo")

print("\n9. print() puede tener varios argumentos:")
print("   Ejemplo:")
print("   print('Hola', 'Mundo')")

print("\n10. Python agrega un espacio entre argumentos automáticamente:")
print("Hola", "Mundo")

print("\n11. Argumento sep:")
print("   Cambia el separador entre los argumentos.")
print("Ejemplo:")
print("Python", "es", "genial", sep="-")

print("\n12. Argumento end:")
print("   Cambia lo que aparece al final de la línea.")
print("Ejemplo:")
print("Hola", end=" ")
print("Mundo")

print("\n13. print() también puede usarse vacío para crear una línea en blanco:")
print("Linea 1")
print()
print("Linea 2")

