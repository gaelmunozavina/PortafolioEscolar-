print("=== Literales en Python ===")

# Cadenas (string)
print("Esto es una cadena")
print("Me gusta \"Monty Python\"")
print('I\'m learning Python')

# Enteros (int)
print("=== Enteros ===")
print(123)
print(-50)
print(11_111_111)  # uso de guion bajo para leer mejor números grandes

# Octal y Hexadecimal
print("=== Octal y Hexadecimal ===")
print(0o123)  # número octal
print(0x123)  # número hexadecimal

# Flotantes (float)
print("=== Flotantes ===")
print(2.5)
print(-0.4)
print(4.0)
print(.4)

# Notación científica
print("=== Notación científica ===")
print(3E8)
print(6.62607E-34)

# Booleanos
print("=== Booleanos ===")
print(True)
print(False)

# Comparaciones booleanas
print("=== Comparaciones ===")
print(True > False)
print(True < False)

# Literal None
print("=== Literal None ===")
print(None)
